# if 조건문에 숫자 지정하기
# 숫자는 정수, 실수와 관곙벗이 0이면 거짓, 아니면 참
if 0:
    print("참")
else:
    print("거짓")

if 1:
    print("참")
else:
    print("거짓")

if 0x1F:
    print("참")
else:
    print("거짓")

if 0b1000:
    print("참")
else:
    print("거짓")

if 13.5:
    print("참")
else:
    print("거짓")

# if 조건문에 문자열 지정하기
# 문자열은 내용이 있을 때 참, 빈 문자열은 거짓
if 'hello':
    print("참")

else:
    print("거짓")

if '':
    print("참")
else:
    print("거짓")
    